package com.mass.biz.smart.topic.model;

/**
 * 频道的实体类
 * @author computer
 *
 */
public class TopicType {
	
	private Long rid;   //主键
	
	private String typeName; //频道名称
	
	private String createTime; //创建时间
	
	private String updateTime;  //更新时间
	
	private Integer delFalg;  //删除标识(0.正常，1.删除)
	
	private String describeType;  //描述

	public Long getRid() {
		return rid;
	}

	public void setRid(Long rid) {
		this.rid = rid;
	}

	public String getTypeName() {
		return typeName;
	}

	public void setTypeName(String typeName) {
		this.typeName = typeName;
	}

	public String getCreateTime() {
		return createTime;
	}

	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}

	public String getUpdateTime() {
		return updateTime;
	}

	public void setUpdateTime(String updateTime) {
		this.updateTime = updateTime;
	}

	public Integer getDelFalg() {
		return delFalg;
	}

	public void setDelFalg(Integer delFalg) {
		this.delFalg = delFalg;
	}

	public String getDescribeType() {
		return describeType;
	}

	public void setDescribeType(String describeType) {
		this.describeType = describeType;
	}
	
	
}
