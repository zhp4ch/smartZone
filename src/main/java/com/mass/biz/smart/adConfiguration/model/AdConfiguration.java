package com.mass.biz.smart.adConfiguration.model; 
/**
 * 类描述：广告配置类
 * 创建人：yihai Zhao
 */
public class AdConfiguration {
	
	private Long id;		//主键
	
	private Integer type;	//类型 0-视频 1-图片
	
	private Double videoLength;//视频长度
	
	private String url;		//资源地址
	
	private Integer delFlag;	//删除标记
	
	private String createTime;	//创建时间
	
	private String explain;		//说明
	
	private Integer state;		//2代表设置视频播放 1代表设置图片播放 	
	
	public Integer getState() {
		return state;
	}

	public void setState(Integer state) {
		this.state = state;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Integer getType() {
		return type;
	}

	public void setType(Integer type) {
		this.type = type;
	}

	public Double getVideoLength() {
		return videoLength;
	}

	public void setVideoLength(Double videoLength) {
		this.videoLength = videoLength;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public Integer getDelFlag() {
		return delFlag;
	}

	public void setDelFlag(Integer delFlag) {
		this.delFlag = delFlag;
	}

	public String getCreateTime() {
		return createTime;
	}

	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}

	public String getExplain() {
		return explain;
	}

	public void setExplain(String explain) {
		this.explain = explain;
	}
}
