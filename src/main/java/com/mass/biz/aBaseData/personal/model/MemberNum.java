public class MemberNum {
	private Integer empNum;   		 //职工人数
	private Integer empUnionNum;    //工会会员数
	private Integer empWomenNum;    //女职工人数
	private Integer empWomenUnionNum; //女职工会员数
	private Integer empLaborNum;    //劳务派遣人数
	private Integer empLaborUnionNum;  //劳务派遣会员数
	
	public Integer getEmpNum() {
		return empNum;
	}
	public void setEmpNum(Integer empNum) {
		this.empNum = empNum;
	}
	public Integer getEmpUnionNum() {
		return empUnionNum;
	}
	public void setEmpUnionNum(Integer empUnionNum) {
		this.empUnionNum = empUnionNum;
	}
	public Integer getEmpWomenNum() {
		return empWomenNum;
	}
	public void setEmpWomenNum(Integer empWomenNum) {
		this.empWomenNum = empWomenNum;
	}
	public Integer getEmpWomenUnionNum() {
		return empWomenUnionNum;
	}
	public void setEmpWomenUnionNum(Integer empWomenUnionNum) {
		this.empWomenUnionNum = empWomenUnionNum;
	}
	public Integer getEmpLaborNum() {
		return empLaborNum;
	}
	public void setEmpLaborNum(Integer empLaborNum) {
		this.empLaborNum = empLaborNum;
	}
	public Integer getEmpLaborUnionNum() {
		return empLaborUnionNum;
	}
	public void setEmpLaborUnionNum(Integer empLaborUnionNum) {
		this.empLaborUnionNum = empLaborUnionNum;
	}
}
